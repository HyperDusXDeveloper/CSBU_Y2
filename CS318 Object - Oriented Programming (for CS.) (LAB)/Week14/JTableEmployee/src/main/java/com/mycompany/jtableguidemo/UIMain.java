    public boolean writeObjectToFile(List<Employee>userData)
    {
        String filePath = "C:\\Users\\Admins\\OneDrive\\HyperDus Develper\\CSBUY1\\CS318 Java OOP\\Week14\\JTableEmployee\\Employee.bin"  ;
        System.err.println("Save File Done " +filePath);
        
        try{
            
            
            FileOutputStream file = new FileOutputStream(filePath) ;
            ObjectOutputStream writer = new ObjectOutputStream(file) ;
            writer.writeObject(userData) ;
            writer.close() ;
            file.close() ;
            return true ;
        }catch(Exception ex)
        {
            System.err.println("failed to write " +filePath+","+ex);
            return false ;
        }
    }
    
    public List<Employee> readUserDataFromFile()
    {
         String filePath = "C:\\Users\\Admins\\OneDrive\\HyperDus Develper\\CSBUY1\\CS318 Java OOP\\Week14\\JTableEmployee\\Employee.bin" ;
         System.err.println("Load File Done " +filePath);
        
        try{
            FileInputStream file = new FileInputStream(filePath) ;
            ObjectInputStream reader = new ObjectInputStream(file) ;
            while(true)
            {
                try{
                    List<Employee>obj = (List<Employee>)reader.readObject() ;
                    return obj ;
                }catch(Exception ex){
                    System.err.println("end of read file");
                    break ;
                }
            }
        }catch(Exception ex)
        {
            System.err.println("failed to write " +filePath+","+ex);
        }
        return null ;
    }
    

