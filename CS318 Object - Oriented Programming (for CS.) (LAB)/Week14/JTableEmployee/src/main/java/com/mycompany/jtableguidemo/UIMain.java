    private void btnLoadfromFileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoadfromFileMouseClicked
        // TODO add your handling code here:
        UserList = readUserDataFromFile() ;
        
        DefaultTableModel model = (DefaultTableModel)tableData.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged() ;
        for(Employee xRow : UserList)
        {            
        model.addRow(new Object[0]);
        model.setValueAt(xRow.getID(),model.getRowCount()-1,0) ;
        model.setValueAt(xRow.getFName(),model.getRowCount()-1,1) ;
        model.setValueAt(xRow.getLName(),model.getRowCount()-1,2) ;
        model.setValueAt(xRow.getPhoneNo(),model.getRowCount()-1,3) ;
        model.setValueAt(xRow.getEmail(),model.getRowCount()-1,4) ;
        model.setValueAt(xRow.getSalary(),model.getRowCount()-1,5) ;
        model.setValueAt(xRow.getJobPosition(),model.getRowCount()-1,6) ;
        }
    }//GEN-LAST:event_btnLoadfromFileMouseClicked

