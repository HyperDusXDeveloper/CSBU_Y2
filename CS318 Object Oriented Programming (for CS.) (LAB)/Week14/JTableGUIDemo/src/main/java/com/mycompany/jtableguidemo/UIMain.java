    private void btnAddtoTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddtoTableMouseClicked
        // TODO add your handling code here:
        UserData user = new UserData() ;
        user.setFName(txtFirstName.getText()) ;
        user.setLName(txtLastName.getText()) ;
        user.setPhoneNo(txtPhoneNo.getText()) ;
        user.setEmail(txtEmail.getText()) ;
        UserList.add(user) ;
        
        DefaultTableModel model = (DefaultTableModel)tableData.getModel();
        
        model.addRow(new Object[0]);
        model.setValueAt(user.getFName(),model.getRowCount()-1,0) ;
        model.setValueAt(user.getLName(),model.getRowCount()-1,1) ;
        model.setValueAt(user.getPhoneNo(),model.getRowCount()-1,2) ;
        model.setValueAt(user.getEmail(),model.getRowCount()-1,3) ;
    }//GEN-LAST:event_btnAddtoTableMouseClicked

