    public boolean writeObjectToFile(List<UserData>userData)
    {
        String filePath = "D:\\UserData.bin" ;
        
        try{
            
            
            FileOutputStream file = new FileOutputStream(filePath) ;
            ObjectOutputStream writer = new ObjectOutputStream(file) ;
            writer.writeObject(userData) ;
            writer.close() ;
            file.close() ;
            return true ;
        }catch(Exception ex)
        {
            System.err.println("failed to write " +filePath+","+ex);
            return false ;
        }
    }
    
    public List<UserData> readUserDataFromFile()
    {
         String filePath = "D:\\UserData.bin" ;
        
        try{
            FileInputStream file = new FileInputStream(filePath) ;
            ObjectInputStream reader = new ObjectInputStream(file) ;
            while(true)
            {
                try{
                    List<UserData>obj = (List<UserData>)reader.readObject() ;
                    return obj ;
                }catch(Exception ex){
                    System.err.println("end of read file");
                    break ;
                }
            }
        }catch(Exception ex)
        {
            System.err.println("failed to write " +filePath+","+ex);
        }
        return null ;
    }
    

